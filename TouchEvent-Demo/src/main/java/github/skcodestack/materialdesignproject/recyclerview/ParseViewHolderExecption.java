public class ParseViewHolderExecption extends Exception {

    public ParseViewHolderExecption() {
    }

    public ParseViewHolderExecption(String message) {
        super(message);
    }

    public ParseViewHolderExecption(String message, Throwable cause) {
        super(message, cause);
    }

    public ParseViewHolderExecption(Throwable cause) {
        super(cause);
    }

    public ParseViewHolderExecption(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
